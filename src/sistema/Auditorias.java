    //Metodo para Cargar tabla
    //************************
    private void CargarTabla(){
        //Arreglo para los Titulos
        //***********************
        String [] titulos = {"Codigo", "Usuario", "Nombre","Apellido", "Fecha", "Hora", "Modulo"};
        //Arreglo para los Registro
        //************************
        String [] registros = new String [7];
        //Creamos el Modelo de la Tabla
        //*****************************
        MODELO = new DefaultTableModel(null, titulos);
        //Creamos Conexion
        //****************
        ConexionMySQL mysql = new ConexionMySQL();
        Connection cn = mysql.Conectar();
        //Creamos Sentencia SQL
        //*********************
        String SQL = "";
        SQL += "SELECT a.id, u.usuario, u.nombres, u.apellidos, a.fecha, a.hora, m.denominacion ";
        SQL += "from auditoria as a, usuarios as u, modulos as m ";
        SQL += "where a.user = u.cedula and a.modulo = m.id ";
        //Ejecutamos Accion
        //*****************
        try{
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while(rs.next()){
                registros[0] = rs.getString(1);
                registros[1] = rs.getString(2);
                registros[2] = rs.getString(3);
                registros[3] = rs.getString(4);
                registros[4] = rs.getString(5);
                registros[5] = rs.getString(6);
                registros[6] = rs.getString(7);
                MODELO.addRow(registros);
            }
            tblAuditoria.setModel(MODELO);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }

