    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        if (txtUsuario.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese su Usuario...!");
            txtUsuario.requestFocus();
        } else if (txtClave.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese su Clave...");
            txtClave.requestFocus();
        } else {
            Users u = new Users(this.txtUsuario.getText(), String.copyValueOf(this.txtClave.getPassword()));
            //Usuario y Password Valido ?
            //***************************
            if (!u.isValidUser()) {
                u.Limpiar();
                //Mas de tres veces sale del sistema
                //**********************************
                if (++nIntentos > 3) {
                    JOptionPane.showMessageDialog(null, "ERROR:\nSaldra de la aplicaci칩n.\nHa excedido el numero de intentos!!!\n\nConsulte al Administrador del Sistema.", "Sistema", JOptionPane.ERROR_MESSAGE);
                    System.exit(0);
                } else {
                    JOptionPane.showMessageDialog(null,"ERROR:\nUsuario o Contrase침a invalido.\n\nIntente de nuevo por favor.", "Sistema", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                //JOptionPane.showMessageDialog(null, "equipo " + VAR);
                USUARIO = txtUsuario.getText();
                Principal frm = new Principal();
                frm.setVisible(true);
                this.dispose();
            }
        }
}//GEN-LAST:event_btnAceptarActionPerformed

