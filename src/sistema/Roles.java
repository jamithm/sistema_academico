    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtRol.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese Campo Rol...!");
            txtRol.requestFocus();
        } 
        else{

        ConexionMySQL mysql = new ConexionMySQL();
        Connection cn = mysql.Conectar();

        String pais = txtRol.getText();
        String sSQL = "";
        String  mensaje = "";

        if(accion.equals("Insertar")){
            sSQL = "INSERT INTO roles (denominacion) VALUES(?)";
            mensaje = "Registro Guardado...!";
        }
        else if(accion.equals("Modificar")){
            sSQL = "UPDATE roles SET denominacion = ? WHERE id = " + id_act;
            mensaje = "Registro Actualizado...!";
        }
       
        try{
            PreparedStatement pst = cn.prepareStatement(sSQL);
            pst.setString(1, pais);

            int n = pst.executeUpdate();
            if(n == 1){
                //JOptionPane.showMessageDialog(null, mensaje);

            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        CargarTabla("");
        Habilitar();
        Limpiar();
        TamañoTabla();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


    private void tblRolesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRolesMouseClicked
        int filasel;
        String id;

        try {
            filasel = tblRoles.getSelectedRow();
            if(filasel == -1) {
               // JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna fila...!");
            } else {
                modelo = (DefaultTableModel) tblRoles.getModel();
                id = (String) modelo.getValueAt(filasel, 0);
                txtBuscar.setText("");
                CargarDatos(id);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblRolesMouseClicked

